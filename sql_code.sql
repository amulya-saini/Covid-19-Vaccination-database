CREATE DATABASE vaccine_database;

USE vaccine_database;

CREATE TABLE health_records (
  health_id INT PRIMARY KEY,
  weight_kg INT,
  height_cms INT,
  allergies VARCHAR(255),
  alcohol ENUM('yes', 'no'),
  smoking ENUM('yes', 'no'),
  substance_abuse ENUM('yes', 'no'),
  chronic_conditions CHAR(255),
  Treatment ENUM('yes', 'no'),
  Medications CHAR(255),
  surgical_history CHAR(255),
  issued_id VARCHAR(255)
);

CREATE TABLE Vaccination_center (
  center_id VARCHAR(255) PRIMARY KEY,
  address VARCHAR(255),
  state VARCHAR(255),
  country VARCHAR(255),
  vaccine_brand VARCHAR(255),
  vaccine_adminitrated VARCHAR(255),
  stock INT
);

CREATE TABLE Side_effects (
  se_id INT PRIMARY KEY,
  adverse_effect VARCHAR(255),
  onset_date DATE,
  end_date DATE,
  treated ENUM('yes', 'no'),
  subsided ENUM('yes', 'no'),
  medications VARCHAR(255),
  complication VARCHAR(255),
  symptoms VARCHAR(255),
  surgery ENUM('yes', 'no'),
  surgery_name VARCHAR(255)
);

CREATE TABLE vaccination_information (
  vaccine_id VARCHAR(255) PRIMARY KEY,
  vaccine_name VARCHAR(255),
  manufacturer VARCHAR(255),
  batch_no INT,
  dose_1 DATE,
  dose_2 DATE,
  booster_dose DATE,
  administrator_name VARCHAR(255),
  center_id VARCHAR(255),
  se_id INT,
  FOREIGN KEY (center_id) REFERENCES vaccination_center(center_id),
  FOREIGN KEY (se_id) REFERENCES Side_effects(se_id)
);

CREATE TABLE Personal_details (
  Cov_id VARCHAR(255) PRIMARY KEY,
  first_name VARCHAR(255),
  last_name VARCHAR(255),
  date_of_birth DATE,
  age INT,
  sex_at_birth ENUM('male', 'female'),
  address VARCHAR(255),
  state VARCHAR(255),
  country VARCHAR(255),
  health_id INT,
  vaccine_id VARCHAR(255),
  FOREIGN KEY (health_id) REFERENCES Health_records(health_id),
  FOREIGN KEY (vaccine_id) REFERENCES vaccination_information(Vaccine_id)
);

